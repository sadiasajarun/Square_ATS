/* Pipeline stage — derived from the score band so it can never contradict it.
   REJECTED_PENDING_REVIEW stays distinct from REJECTED: below-threshold
   candidates are pending a human decision, never auto-rejected. */
function stageOf(c,r){
  if(c.sc>=80)return r<0.45?'SHORTLISTED':(r<0.75?'IN_INTERVIEW':'ADVANCED');
  if(c.sc>=75)return 'SHORTLISTED';
  if(c.sc>=50)return r<0.7?'SCREENED':'RETURNED_TO_HR';
  return 'REJECTED_PENDING_REVIEW';
}
