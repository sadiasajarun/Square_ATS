/* One-line AI evaluation summary — assembled from the candidate's own
   attributes so it can never contradict the chips beside it. */
function summary(c,cfg){
  var s=(c.sc>=85?'Strong':c.sc>=70?'Solid':c.sc>=50?'Partial':'Weak')+' '+cfg.dept.toLowerCase()+' fit ('+c.yr+' yrs';
  if(c.co)s+=', '+c.co;
  s+=')';
  var cov=Math.round(c.sk.length/SKILLS.length*100);
  s+='; covers '+c.sk.length+' of '+SKILLS.length+' required skills';
  var miss=SKILLS.filter(function(k){return c.sk.indexOf(k)<0});
  if(miss.length&&miss.length<=2)s+='; gap in '+miss.join(' & ');
  else if(cov===100)s+=' — full coverage';
  if(c.rf.length)s+='; flag: '+c.rf[0].toLowerCase();
  return s+'.';
}

/* Per-criterion screening explanation — the explainability artifact.
   Contributions are derived from the candidate's own skills/experience so the
   weighted sum lands on their actual match score. */
var CRITERIA=[
  {k:'Skills match',w:35},
  {k:'Years of experience',w:20},
  {k:'Education',w:15},
  {k:'Past positions',w:15},
  {k:'Location',w:10},
  {k:'Certifications',w:5}
];
function explain(c){
  var cov=c.sk.length/SKILLS.length;
  var expF=Math.min(1,c.yr/8);
  var edF=c.ed==='PhD'?1:(c.ed==="Master's / MBA"?0.9:0.65);
  var posF=Math.min(1,0.5+c.sk.length*0.1);
  var locF=c.d==='Dhaka'?1:0.6;
  var certF=c.rf.indexOf('Missing information')>-1?0.3:0.7;
  var f=[cov,expF,edF,posF,locF,certF];
  var raw=CRITERIA.reduce(function(a,cr,i){return a+cr.w*f[i]},0);
  var scale=raw?c.sc/raw:1;   /* normalise so contributions sum to the real score */
  return CRITERIA.map(function(cr,i){
    var got=Math.round(cr.w*f[i]*scale*10)/10;
    var missed=SKILLS.filter(function(k){return c.sk.indexOf(k)<0});
    var ev=i===0?('matched: '+c.sk.join(', ')+(missed.length?' | missed: '+missed.join(', '):''))
      :i===1?(c.yr+' yrs of experience parsed from CV')
      :i===2?(c.ed+' parsed from CV')
      :i===3?(c.role+' at '+c.co)
      :i===4?(c.d+(c.d==='Dhaka'?' — in preferred location':' — outside preferred location'))
      :(c.rf.indexOf('Missing information')>-1?'certification section incomplete':'no certification gaps detected');
    return {crit:cr.k,weight:cr.w,contribution:got,evidence:ev};
  });
}

